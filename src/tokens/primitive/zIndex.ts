  sticky: 100,
  /** 200 — オーバーレイ背景（scrim） */
  overlay: 200,
  /** 300 — モーダル、ダイアログ */
  modal: 300,
  /** 400 — ポップオーバー、ドロップダウン、ツールチップ */
  popover: 400,
